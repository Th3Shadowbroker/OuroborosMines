package dev.th3shadowbroker.ouroboros.mines.events;

import dev.th3shadowbroker.ouroboros.mines.regions.MiningRegion;
import org.bukkit.block.Block;
import org.bukkit.entity.Player;
import org.bukkit.event.Cancellable;
import org.bukkit.event.Event;
import org.bukkit.event.HandlerList;

public class RegionCheckEvent extends Event implements Cancellable {

    private final Player player;

    private final MiningRegion region;

    private final Block block;

    private boolean cancelled;

    private static final HandlerList handlerList = new HandlerList();

    public RegionCheckEvent(Player player, MiningRegion region, Block block) {
        this.player = player;
        this.region = region;
        this.block = block;
        this.cancelled = false;
    }

    public Player getPlayer() {
        return player;
    }

    public MiningRegion getRegion() {
        return region;
    }

    public Block getBlock() {
        return block;
    }

    @Override
    public HandlerList getHandlers() {
        return handlerList;
    }

    public static HandlerList getHandlerList() {
        return handlerList;
    }

    @Override
    public boolean isCancelled() {
        return cancelled;
    }

    @Override
    public void setCancelled(boolean b) {
        cancelled = b;
    }

}
